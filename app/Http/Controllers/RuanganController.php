    {
        try{
            Ruang::create([
                'nama_ruang' => $request->nama_ruang,
                'jenis_ruang'=>$request->jenis_ruang
            ]);
             
            return redirect()->back()->with('success', 'Ruangan Berhasil Ditambah!');
            }
            catch (\Exception $e) {
    
                return redirect()->back()->with('warning', 'Ruangan sudah tersedia');
                
            }
    }

